package com.vlearntech.java.step03datatypes;

public class DataTypes {
	public static void main(String[] args) {

		boolean isMarried = true;

		char gradeInExam = 'A';

		byte noOfStudents = 100;

		short noOfInvitesForParty = 450;

		int salary = 50_000;

		long populationOfContinent = 4_000_000_000L; // If you enter a number beyond 2.14 b (Integer.MAX_VALUE), you
														// need to add a l or L at the end of the numeric literal.

		float avgAge = 23.6f; // Decimals are by default "double", and hence you need "f" to be added at the end to
								// indicate that it is a float value

		double avgSalary = 45342.56;

	}
}
